package oo.abstrato;

public abstract class  Abstrata {
    /*
    * Uma classe abstrata pode ter ou não métodos concretos, ou seja implementados.
    * Uma classe pode ter todos os métodos abstratos, todos os métodos concretos ou uma mistura deles.
    * Uma classe abstrata não pode ser instaciada!!!
    * Só podem existir métodos abstratos em classes abstratas
    */

    public abstract void metodoAbstrato();
}
